    public void cargarTabla() {

        ArrayList<EntidadDto> cad=this.menu.getPrincipal().getFachada().consultarEntidades();
        String[] nomC = new String[5];
        nomC[0] = "Codigo";
        nomC[1] = "Nombre";
        nomC[2] = "Sufijo";
        nomC[3] = "Direccion";
        nomC[4] = "Telefono";

        String datos[][] = new String[cad.size()][5];
        for (int i = 0; i < cad.size(); i++) {
            datos[i][0]=cad.get(i).getCodigo_entidad();
            datos[i][1]=cad.get(i).getNombre_entidad();
            datos[i][2]=cad.get(i).getSufijo();
            datos[i][3]=cad.get(i).getDireccion();
            datos[i][4]=cad.get(i).getTelefono();
            }
           this.TableEntidad.setModel(new DefaultTableModel(datos, nomC));
        }
     
    

