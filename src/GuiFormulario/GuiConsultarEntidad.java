    private boolean cerrar() {
        Object[] opciones = {"Aceptar", "Cancelar"};
        int eleccion = JOptionPane.showOptionDialog(rootPane, "Esta seguro que desea borrar la entidad. ", "Mensaje de Confirmacion",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE, null, opciones, "Cancelar");

        return (eleccion == JOptionPane.YES_OPTION);

    }

    private String[] valorTabla() {

        if (this.TableEntidad.getSelectedRow() >= 0) {

            String datos[] = {
                String.valueOf(this.TableEntidad.getValueAt(this.TableEntidad.getSelectedRow(), 0)),
                String.valueOf(this.TableEntidad.getValueAt(this.TableEntidad.getSelectedRow(), 1)),
                String.valueOf(this.TableEntidad.getValueAt(this.TableEntidad.getSelectedRow(), 2)),
                String.valueOf(this.TableEntidad.getValueAt(this.TableEntidad.getSelectedRow(), 3)),
                String.valueOf(this.TableEntidad.getValueAt(this.TableEntidad.getSelectedRow(), 4))
            };

            return datos;
        }
        return null;

    }

