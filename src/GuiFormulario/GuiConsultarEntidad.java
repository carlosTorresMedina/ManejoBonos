    private void TableEntidadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableEntidadMouseClicked
        // TODO add your handling code here:
        String[] datos = this.valorTabla();
        int codigo = Integer.parseInt(datos[0]);
        int seleccion = JOptionPane.showOptionDialog(
                rootPane,
                "Seleccione opcion a realizar para la entidad: " + codigo,
                "Selector de opciones ",
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                new Object[]{"Modifircar", "   Borrar", "Cancelar"},
                "opcion 1");

        if (seleccion == 0) {

            GuiModificarEntidad modif = new GuiModificarEntidad(this.menu, codigo + "", datos[1], datos[2], datos[3], datos[4]);
            this.menu.getPrincipal().personalizar(modif);
            this.setVisible(false);
            modif.setVisible(true);
            return;
        }
        if (seleccion == 1) {
            boolean confirmacion = this.cerrar();
            if (confirmacion) {

                boolean x = this.menu.getPrincipal().getFachada().borrarEntidad(codigo);
                if (x) {
                    JOptionPane.showMessageDialog(null, "el registro se borro con exito");
                    this.cargarTabla();
                } else {
                    JOptionPane.showMessageDialog(null, "la entidad no se puede borrar \n"
                            + "tiene bonos asociados");
                }
            } else {
                return;
            }

        }

    }//GEN-LAST:event_TableEntidadMouseClicked

